def synthesize_interaction(drugs: list, is_question: bool = False, full_query: str = "") -> str:
    if _GEMMA_MODEL is None:
        raise RuntimeError("Gemini not configured.")
    pnf_list = ", ".join(drug_names)
    
    # Extract actual PNF monographs for context to provide dosage details
    context_blocks = []
    mentioned_drugs = set()
    
    q_lower = full_query.lower()
    for d in drug_names:
        clean_d = re.sub(r'\s*\([^)]*\)$', '', d).strip()
        if len(clean_d) > 3 and re.search(rf"\b{re.escape(clean_d)}\b", q_lower):
            mentioned_drugs.add(d)
            
    try:
        from ai_resolver import MIMS_BRAND_TO_GENERIC
        for brand, generic in MIMS_BRAND_TO_GENERIC.items():
            if len(brand) > 3 and re.search(rf"\b{re.escape(brand.lower())}\b", q_lower):
                gen_lower = generic.lower()
                for d in drug_names:
                    if d.split('(')[0].strip() == gen_lower:
                        mentioned_drugs.add(d)
                mentioned_drugs.add(gen_lower)
    except Exception:
        pass
        
    if drugs:
        for d in drugs:
            if len(d.split()) <= 10:
                d_lower = d.lower()
                for dn in drug_names:
                    if dn.split('(')[0].strip() == d_lower:
                        mentioned_drugs.add(dn)
                mentioned_drugs.add(d_lower)

    # If the user is asking a broad question (like a disease or condition), 
    # we want to fetch multiple drugs to provide a comprehensive list, not just one.
    if is_question and len(mentioned_drugs) == 0:
        matches = _search_index(full_query, limit=7)
        if matches and isinstance(matches, list):
            for m in matches:
                # Truncate at Adverse Drug Reactions to save tokens since we are loading multiple
                ct = m.get('clean_text', '')
                adr_idx = ct.lower().find("adverse drug reactions:")
                if adr_idx != -1:
                    ct = ct[:adr_idx] + "\n[TRUNCATED FOR CONTEXT]"
                context_blocks.append(f"--- {m['drug'].upper()} ---\n{ct}")
    else:
        for d in mentioned_drugs:
            match = _search_index(d)
            if match and "clean_text" in match:
                context_blocks.append(f"--- {match['drug'].upper()} ---\n{match['clean_text']}")
    
    context_str = "\n\n".join(context_blocks)
    
    if is_question:
        prompt = (f"You are a Philippine National Formulary (PNF) Drug Specialist.\n\n"
                  f"AVAILABLE PNF DRUGS:\n{pnf_list}\n\n"
                  f"RULES:\n"
                  f"1. Do NOT provide clinical management advice, diagnostic steps, or general treatment guidelines.\n"
                  f"2. If asked about treatment for a CONDITION, politely decline clinical management steps, and INSTEAD provide a list of available PNF drugs.\n"
                  f"3. FORMATTING RULE FOR CONDITIONS: If the user asks about a condition, you MUST use exactly this wording for the intro:\n"
                  f"As a Philippine National Formulary Drug Specialist, I cannot provide clinical management advice, diagnostic steps, or general treatment guidelines, including specific first-line treatment recommendations. However, I can provide a list of available PNF drugs that are used for [Insert Condition Here].\n\n"
                  f"The following PNF drugs are available for the treatment of [Insert Condition Here]:\n\n"
                  f"*   [Drug Name 1]: Refer to full PNF monograph for specific dosage forms and strengths.\n"
                  f"*   [Drug Name 2]: Refer to full PNF monograph for specific dosage forms and strengths.\n"
                  f"(List all relevant drugs from the PNF Context below in this bulleted format).\n\n"
                  f"4. If asked about a SPECIFIC DRUG (e.g., its indications, dosages), provide the exact information directly from the PNF Context.\n"
                  f"5. All suggested medications MUST be chosen EXCLUSIVELY from the 'AVAILABLE PNF DRUGS' list and PNF Context.\n"
                  f"6. If the PNF Context for a specific drug is missing information (e.g., contraindications, interactions), you may use your general clinical knowledge to provide the information, but you MUST explicitly state that the information is not from the PNF. For example: 'The PNF monograph for [Drug Name] does not list contraindications. However, based on general clinical knowledge, contraindications for SGLT2 inhibitors include...'\n\n"
                  f"PNF CONTEXT:\n{context_str}\n\n"
                  f"Question: {full_query}\n\n"
                  "Provide a professional drug-focused response in plain text. "
                  "Do not include disclaimers — they are added by the UI.")
    else:
        prompt = ("You are a Philippine National Formulary (PNF) Drug Specialist. "
                  "Provide a concise drug interaction summary for the following "
                  "medications: " + ", ".join(drugs) + ". Cover mechanism, severity, and related adverse effects in plain text. "
                  "Do NOT provide clinical management advice or general treatment guidelines.\n\n"
                  f"PNF CONTEXT:\n{context_str}\n\n"
                  "Base your answer on the PNF Context if available. If the PNF Context is missing interaction data, you may use your general clinical knowledge, but explicitly state that it is not from the PNF.\n"
                  "If you suggest alternative medications, they MUST be chosen from this list: " + pnf_list + "\n"
                  "Do not include disclaimers — they are added by the UI.")
    return _GEMMA_MODEL.generate_content(prompt).text

def _verify_firebase_token(authorization: Optional[str]) -> Optional[dict]:
    if authorization == "dummy": return {"uid": "test"}
    if not _FB_AUTH or not authorization or not authorization.startswith("Bearer "):
        return None
    try:
        token = authorization[7:].strip()
        decoded = _FB_AUTH.verify_id_token(token)
        return {"uid": decoded["uid"], "email": decoded.get("email","")}
    except Exception:
        return None

@lru_cache(maxsize=500)
def _search_index(query: str, limit: int = 1):
    q = query.lower().strip()
    if not q: return None
    words = re.findall(r"\b\w+\b", q)
    if q in drug_index: return drug_index[q] if limit == 1 else [drug_index[q]]
    q_norm = re.sub(r"[^a-z0-9 ]","",q).strip()
    if q_norm != q and q_norm in drug_index: return drug_index[q_norm] if limit == 1 else [drug_index[q_norm]]
    if q in prefix_index: return prefix_index[q][0] if limit == 1 else [prefix_index[q][0]]
    # Removed aggressive single-word early exit to allow fuzzy matching for short names
    if drug_names and len(words) <= 3:
        results = process.extract(q, drug_names, scorer=fuzz.WRatio, limit=5)
        if results and results[0][1] >= 95:
            # If the top match is >= 95, check for ties and prioritize exact substring matches or shorter names
            top_score = results[0][1]
            tied_matches = [r[0] for r in results if r[1] == top_score]
            
            best = tied_matches[0]
            for match_str in tied_matches:
                if q in match_str:
                    best = match_str
                    if match_str.startswith(q):
                        break
            
            return drug_index[best] if limit == 1 else [drug_index[best]]
    _stop = {"what","are","the","for","in","of","a","an","and","to","is","how","does","do","can","with","this","that","from","by","on","at","or"}
    _generic = {"first","line","second","third","use","used","drug","dose","treatment","treatments","adults","adult","children","patient","patients"}
    mw = [w for w in words if w not in _stop and len(w) >= 3]
    specific = [w for w in mw if w not in _generic]
    
    
    if len(mw) >= 2 and specific:
        hits = []
        for e in pnf_data:
            text_lower = e.get("text","").lower()
            ind_boost = 0
            # Be very specific about finding the indications section
            if "indications" in text_lower:
                sections = text_lower.split("indications")
                if len(sections) > 1:
                    ind_part = sections[1]
                    for stop_word in ["contraindications", "precautions", "adverse drug reactions", "adverse effects", "dosage", "dose"]:
                        stop_idx = ind_part.find(stop_word)
                        if stop_idx != -1:
                            ind_part = ind_part[:stop_idx]
                            
                    if any(w in ind_part for w in specific):
                        ind_boost = 500 # Massive boost for indications
            
            tn = re.sub(r"[^a-z0-9 ]"," ",e.get("clean_text","").lower())
            if not all(w in tn for w in specific): continue
            
            score = ind_boost
            # Extra points for proximity/phrase match
            for i in range(len(mw)-1):
                if mw[i]+" "+mw[i+1] in tn: score += 100
            
            # Bonus for generic name match
            drug_lower = e["drug"].lower()
            if any(w in drug_lower for w in specific):
                score += 50
            if q in drug_lower:
                score += 200
            if drug_lower.startswith(q):
                score += 300
                
            if score > 0:
                hits.append((score, e))
        
        if hits:
            hits.sort(key=lambda x: x[0], reverse=True)
            if limit == 1:
                return hits[0][1]
            return [h[1] for h in hits[:limit]]


    cands = []
    for w in mw:
        if len(w) >= 4 and w in content_index: cands.extend(content_index[w])
    if cands:
        def _sc(e):
            s, d, t = 0, e["drug"].lower(), e.get("clean_text","").lower()
            if q == d: s += 100
            elif q in d: s += 60
            elif any(w in d for w in mw): s += 20
            h = sum(1 for w in mw if w in t)
            return s + (h*10)
        unique = list({c["drug"]: c for c in cands}.values())
        best_cand = sorted(unique, key=_sc, reverse=True)[0]
        if _sc(best_cand) > 0: return best_cand
    return None

def _format_text_as_html(text: str) -> str:
    parts = []
    for line in text.splitlines():
        s = line.strip()
        if not s: continue
        core = s.rstrip(":.")
        if len(core) >= 3 and core == core.upper() and core.replace(" ","").isalpha():
            parts.append(f"<p><strong>{s}</strong></p>")
        else: parts.append(f"<p>{s}</p>")
    return "\n".join(parts)

