def _clean_text(raw: str) -> str:
    cleaned = raw.lstrip("﻿")
    return re.sub(r"April.*?\n|https://.*?pnf\.doh\.gov\.ph\n+|ATC CODE\n+.*?\n+|Page \d of \d","",cleaned)

mims_path = os.path.join(BASE_DIR, "data", "mims_brand_generic_names.txt")
if os.path.exists(mims_path):
    with open(mims_path, "r", encoding="utf-8") as _f:
        for line in _f:
            if ":" in line:
                brand, generic = line.split(":", 1)
                brand, generic = brand.strip(), generic.strip().lower()
                if generic in generic_to_brands:
                    generic_to_brands[generic].append(brand)
                else:
                    generic_to_brands[generic] = [brand]

index_path = os.path.join(BASE_DIR, "data", "pnf_index.json")
if os.path.exists(index_path):
    with open(index_path, "r", encoding="utf-8") as _f:
        pnf_data = json.load(_f)
    for _entry in pnf_data:
        _drug = _entry.get("drug","").lower().strip().strip("﻿​\r")
        _text = _entry.get("text","")
        if not _drug: continue
        _clean = _clean_text(_text)
        _entry["clean_text"] = _clean
        drug_index[_drug] = _entry
        _norm = re.sub(r"[^a-z0-9 ]","",_drug).strip()
        if _norm and _norm != _drug: drug_index[_norm] = _entry
        drug_names.append(_drug)
        for _i in range(1, min(len(_drug),10)+1):
            prefix_index.setdefault(_drug[:_i],[]).append(_entry)
        for _w in set(re.findall(r"\b\w+\b",_clean.lower())):
            if len(_w) > 3: content_index.setdefault(_w,[]).append(_entry)

