    user = _verify_firebase_token(authorization)
    if not user: raise HTTPException(401, "Not authenticated")
    if not _FIRESTORE: return {"chats": []}
    try:
        docs = (_FIRESTORE.collection("users").document(user["uid"])
                .collection("chats").order_by("timestamp", direction=firestore.Query.DESCENDING)
                .limit(50).stream())
        chats = []
        for doc in docs:
            d = doc.to_dict()
            chats.append({"id": doc.id, "question": d.get("question",""),
                          "drugs": d.get("drugs",[]), "timestamp": str(d.get("timestamp",""))})
        return {"chats": chats}
    except Exception as e: return {"chats": [], "error": str(e)}

